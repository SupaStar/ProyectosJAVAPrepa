    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String folio,pago,nombre,ap_p,ap_m,fecha;
        folio=jTextField3.getText();
        pago=jTextField2.getText();
        ap_p=jTextField5.getText();
        ap_m=jTextField6.getText();
        nombre=jTextField4.getText();
        fecha=jFormattedTextField1.getText();
        String sql = "INSERT INTO pago_agua (Folio, fecha_pago, Pago,Nombre,Ap_p,Ap_m) VALUES (?,?,?,?,?,?)";
        
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, folio);
            pst.setString(2, fecha);
            pst.setString(3, pago);
            pst.setString(4, nombre);
            pst.setString(5, ap_p);
            pst.setString(6, ap_m);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Pago registrado");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agua.class.getName()).log(Level.SEVERE, null, ex);
        }
        jLabel13.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField3.requestFocus();
        jTextField5.setText("");
        jTextField6.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

