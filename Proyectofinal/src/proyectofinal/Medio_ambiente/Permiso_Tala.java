    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomb,especie,tel,arbt,pago,donacion,cambio,fech,folio,arbd;
        pago=jTextField5.getText();
        nomb=jTextField1.getText();
        tel=jTextField2.getText();
        arbt=jTextField3.getText();
        especie=jTextField4.getText();
        folio=jTextField6.getText();
        fech=jFormattedTextField2.getText();
        arbd=jLabel9.getText();
    String sql = "INSERT INTO permiso_tala (Folio_tala, fecha_don, Telefono,Arbolest,Especie,deuda,Arb_don,nombre) VALUES (?,?,?,?,?,?,?,?)";
        
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, folio);
            pst.setString(2, fech);
            pst.setString(3, tel);
            pst.setString(4, arbt);
            pst.setString(5, especie);
            pst.setString(6, pago);
            pst.setString(7, arbd);
            pst.setString(8, nomb);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Permiso hecho");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Permiso_Tala.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

