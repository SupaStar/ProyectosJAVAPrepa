    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String raz_soc,folio,domicilio,curp,administrador,actividad;
        raz_soc=jTextField1.getText();
        folio=jTextField6.getText();
        domicilio=jTextField2.getText();
        curp=jTextField3.getText();
        administrador=jTextField4.getText();
        actividad=jTextField5.getText();
        String sql = "INSERT INTO rfc (Id_folio, Raz_soc, Domicilio,Admin,Act,curp) VALUES (?,?,?,?,?,?)";
try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, folio);
            pst.setString(2, raz_soc);
            pst.setString(3, domicilio);
            pst.setString(4, administrador);
            pst.setString(5, actividad);
            pst.setString(6, curp);
            
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "RFC registrado");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(RFC.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String valor = JOptionPane.showInputDialog(this,"Introduce el folio a buscar ");
        String mostrar="SELECT * FROM rfc WHERE CONCAT(Id_folio, Raz_soc, Domicilio,Admin,Act,curp) LIKE '%"+valor+"%'";    
    String [] Titulos={"Folio","Nombre-RazSoc","Domicilio","Curp","Nombre admin","Actividad"};
    String []Datos= new String [6];
        DefaultTableModel model = new DefaultTableModel (null,Titulos);     
        try {
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
              Datos[0]= rs.getString("Id_folio");
              Datos[1]= rs.getString("Raz_soc");
              Datos[2]= rs.getString("Domicilio");
              Datos[3]= rs.getString("curp");
              Datos[4]= rs.getString("Admin");
              Datos[5]= rs.getString("Act");
              model.addRow(Datos);
              }
              jTable1.setModel(model);
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

