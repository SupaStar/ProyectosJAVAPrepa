    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String folio,clave,regimen,ubicacion,colonia,propietario,datost,datosc;
        folio=jTextField7.getText();
        clave=jTextField6.getText();
        regimen=(String) jComboBox3.getSelectedItem();
        ubicacion=jTextField1.getText();
        colonia=jTextField2.getText();
        propietario=jTextField3.getText();
        datost=jTextField4.getText();
        datosc=jTextField5.getText();
    String sql = "INSERT INTO predial (Id_pred, Clave_catast, Regimen,Ubicacion,Colonia,Propietario,Datos_terr,Datos_constr) VALUES (?,?,?,?,?,?,?,?)";
        
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, folio);
            pst.setString(2, clave);
            pst.setString(3, regimen);
            pst.setString(4, ubicacion);
            pst.setString(5, colonia);
            pst.setString(6, propietario);
            pst.setString(7, datost);
            pst.setString(8, datosc);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Predial registrado");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Predial.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    String valor = JOptionPane.showInputDialog(this,"Introduce el nombre a buscar ");
        
        String mostrar="SELECT * FROM predial WHERE CONCAT(Id_pred, Clave_catast, Regimen,Ubicacion,Colonia,Propietario,Datos_terr,Datos_constr) LIKE '%"+valor+"%'";    
    String [] Titulos={"Folio","Clave Catast","Regimen","Ubicacion","Colonia","Propietario","Datos terr","Datos constr"};
    String []Datos= new String [8];
    DefaultTableModel model = new DefaultTableModel (null,Titulos);     
        try {
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
              Datos[0]= rs.getString("Id_pred");
              Datos[1]= rs.getString("Clave_catast");
              Datos[2]= rs.getString("Regimen");
              Datos[3]= rs.getString("Ubicacion");
              Datos[4]= rs.getString("Colonia");
              Datos[5]= rs.getString("Propietario");
              Datos[6]= rs.getString("Datos_terr");
              Datos[7]= rs.getString("Datos_constr");
              model.addRow(Datos);
              }
              jTable1.setModel(model);
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

