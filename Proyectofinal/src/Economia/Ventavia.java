    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String fech,nombre,rfc,domicilio,metros,ubicacion,numtel,pago,folio;
        fech=jFormattedTextField1.getText();
        nombre=jTextField1.getText();
        rfc=jTextField2.getText();
        domicilio=jTextField3.getText();
        metros=jTextField4.getText();
        ubicacion=jTextField5.getText();
        numtel=jTextField6.getText();
        pago=jTextField7.getText();
        folio=jTextField8.getText();
        String sql = "INSERT INTO venta_pub (Id_folio, Raz_soc, fecha_perm,rfc,metros,ubicacion,telefono,deuda) VALUES (?,?,?,?,?,?,?,?)";
        
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, folio);
            pst.setString(2, nombre);
            pst.setString(3, fech);
            pst.setString(4, rfc);
            pst.setString(5, metros);
            pst.setString(6, ubicacion);
            pst.setString(7, numtel);
            pst.setString(8, pago);
            
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Permiso hecho");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ventavia.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

